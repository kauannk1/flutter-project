class ClassSlot {
  final int dayOfWeek;
  final String startTime;
  final String endTime;
  final String subject;
  final String professor;
  final String room;

  ClassSlot({
    required this.dayOfWeek,
    required this.startTime,
    required this.endTime,
    required this.subject,
    required this.professor,
    required this.room,
  });
}
